"""
Dataset classes for Decision Transformer training.

Loads trajectory data from NPZ files generated by data/generate_dataset.py
and prepares batches with:
- Augmented states (vehicle + track + obstacle features)
- Actions
- Returns-to-go
- Timesteps

NPZ format (from data/generate_dataset.py):
- s_m: arc-length positions (N+1,)
- X_full: full state array (N+1, 8) - [ux, uy, r, dfz_long, dfz_lat, t, e, dpsi]
- U: control array (N+1, 2) - [delta, Fx]
- dt: time increments (N,)
- reward: -dt (N,)
- rtg: returns-to-go (N,)
- pos_E, pos_N: global positions (N+1,)
- yaw_world: global heading (N+1,)
- kappa, half_width, grade, bank: track features (N+1,)
- s_offset_m: starting position (scalar)
"""

from dataclasses import dataclass
from pathlib import Path
from typing import Dict, List, Optional, Sequence, Tuple, Union
import json

import numpy as np
import torch
from torch.utils.data import Dataset


# State indices in X_full (from TrajectoryOptimizer)
IDX_UX = 0
IDX_UY = 1
IDX_R = 2
IDX_DFZ_LONG = 3
IDX_DFZ_LAT = 4
IDX_T = 5
IDX_E = 6
IDX_DPSI = 7


@dataclass
class DatasetStats:
    """Statistics for normalization."""
    state_mean: np.ndarray
    state_std: np.ndarray
    action_mean: np.ndarray
    action_std: np.ndarray
    rtg_mean: float
    rtg_std: float

    def save(self, path: Union[str, Path]) -> None:
        """Save statistics to file."""
        np.savez(
            path,
            state_mean=self.state_mean,
            state_std=self.state_std,
            action_mean=self.action_mean,
            action_std=self.action_std,
            rtg_mean=np.array([self.rtg_mean]),
            rtg_std=np.array([self.rtg_std]),
        )

    @classmethod
    def load(cls, path: Union[str, Path]) -> "DatasetStats":
        """Load statistics from file."""
        data = np.load(path)
        return cls(
            state_mean=data["state_mean"],
            state_std=data["state_std"],
            action_mean=data["action_mean"],
            action_std=data["action_std"],
            rtg_mean=float(data["rtg_mean"]),
            rtg_std=float(data["rtg_std"]),
        )


class TrajectoryDataset(Dataset):
    """
    Dataset for Decision Transformer training.

    Each item is a random subsequence of length K from a trajectory.
    Loads data lazily from NPZ files.
    """

    def __init__(
        self,
        data_dir: Union[str, Path, Sequence[Union[str, Path]]],
        context_length: int = 30,
        obstacle_slots: int = 8,
        obstacle_lookahead: float = 60.0,
        normalize: bool = True,
        stats: Optional[DatasetStats] = None,
        split: str = "train",
        max_episodes: Optional[int] = None,
    ):
        """
        Initialize dataset.

        Args:
            data_dir: Directory containing episodes/ folder and manifest.jsonl
            context_length: Context window size K
            obstacle_slots: Number of obstacle slots M
            obstacle_lookahead: Arc-length window for obstacle features
            normalize: Whether to normalize states, actions, and RTG
            stats: Pre-computed normalization statistics
            split: Dataset split (used for stat computation if not provided)
            max_episodes: Maximum number of episodes to load (for testing)
        """
        self.data_dir = data_dir
        self.data_dirs = self._resolve_data_dirs(data_dir)
        self.context_length = context_length
        self.obstacle_slots = obstacle_slots
        self.obstacle_lookahead = obstacle_lookahead
        self.normalize = normalize
        self.split = split

        # Dimensions
        self.state_dim = 8          # [ux, uy, r, e, dpsi, pos_E, pos_N, yaw_world]
        self.track_dim = 2          # [kappa, half_width]
        self.obstacle_feat_dim = 3  # [Δs, e_obs - e_k, r_obs]
        self.act_dim = 2            # [delta, Fx]

        # Total augmented state dimension
        self.state_aug_dim = self.state_dim + self.track_dim + self.obstacle_slots * self.obstacle_feat_dim

        # Load manifest
        self.episodes = self._load_manifest(max_episodes)

        # Build index mapping (episode_idx, start_idx) for sampling
        self.indices = []
        for ep_idx, ep in enumerate(self.episodes):
            T = ep["length"]
            for start in range(T):
                self.indices.append((ep_idx, start))

        # Compute or use provided statistics
        if normalize:
            if stats is not None:
                self.stats = stats
            else:
                self.stats = self._compute_statistics()
        else:
            self.stats = None

    def _resolve_data_dirs(
        self,
        data_dir: Union[str, Path, Sequence[Union[str, Path]]],
    ) -> List[Path]:
        """Resolve one or more dataset shard directories."""
        if isinstance(data_dir, (str, Path)):
            raw_dirs = [item.strip() for item in str(data_dir).split(",") if item.strip()]
        else:
            raw_dirs = [str(item).strip() for item in data_dir if str(item).strip()]

        resolved: List[Path] = []
        for raw_dir in raw_dirs:
            path = Path(raw_dir)
            manifest_path = path / "manifest.jsonl"
            if manifest_path.exists():
                resolved.append(path)
                continue

            shard_dirs = sorted(
                p for p in path.iterdir()
                if p.is_dir() and (p / "manifest.jsonl").exists()
            ) if path.exists() else []
            if shard_dirs:
                resolved.extend(shard_dirs)
                continue

            raise ValueError(
                f"Could not resolve dataset shard(s) from {path}. "
                "Expected a shard directory with manifest.jsonl, a comma-separated "
                "list of shard directories, or a root containing shard subdirectories."
            )

        if not resolved:
            raise ValueError("No dataset shard directories were resolved.")

        return resolved

    def _load_manifest(self, max_episodes: Optional[int] = None) -> List[Dict]:
        """Load episode metadata from one or more shard manifest files."""
        episodes = []

        for shard_dir in self.data_dirs:
            manifest_path = shard_dir / "manifest.jsonl"
            if manifest_path.exists():
                with open(manifest_path, "r") as f:
                    for line in f:
                        if not line.strip():
                            continue
                        ep = json.loads(line)
                        npz_path = Path(ep["npz_path"])
                        if npz_path.exists():
                            data = np.load(npz_path)
                            ep["length"] = len(data["rtg"])
                            ep["obstacles_list"] = ep.get("obstacles", [])
                            episodes.append(ep)
                        if max_episodes and len(episodes) >= max_episodes:
                            break
            else:
                episodes_dir = shard_dir / "episodes"
                if episodes_dir.exists():
                    for npz_file in sorted(episodes_dir.glob("*.npz")):
                        data = np.load(npz_file)
                        episodes.append({
                            "npz_path": str(npz_file),
                            "length": len(data["rtg"]),
                            "obstacles_list": [],
                        })
                        if max_episodes and len(episodes) >= max_episodes:
                            break

            if max_episodes and len(episodes) >= max_episodes:
                break

        if not episodes:
            raise ValueError(f"No episodes found in resolved dataset shards: {self.data_dirs}")

        print(f"Loaded {len(episodes)} episodes from {len(self.data_dirs)} shard(s)")
        return episodes

    def _load_episode(self, ep_idx: int) -> Dict[str, np.ndarray]:
        """Load episode data from NPZ file."""
        return self._load_episode_from_record(self.episodes[ep_idx])

    def _load_episode_from_record(self, ep: Dict) -> Dict[str, np.ndarray]:
        """Load episode data from an episode manifest record."""
        npz_path = Path(ep["npz_path"])
        data = np.load(npz_path)

        # Extract vehicle observation from X_full
        # X_full: (N+1, 8) - [ux, uy, r, dfz_long, dfz_lat, t, e, dpsi]
        X_full = data["X_full"]

        # Build DT observation: [ux, uy, r, e, dpsi, pos_E, pos_N, yaw_world]
        ux = X_full[:, IDX_UX]
        uy = X_full[:, IDX_UY]
        r = X_full[:, IDX_R]
        e = X_full[:, IDX_E]
        dpsi = X_full[:, IDX_DPSI]
        pos_E = data["pos_E"]
        pos_N = data["pos_N"]
        yaw_world = data["yaw_world"]

        # Ensure all arrays have same length (use rtg length as reference)
        T = len(data["rtg"])

        states = np.column_stack([
            ux[:T], uy[:T], r[:T], e[:T], dpsi[:T],
            pos_E[:T], pos_N[:T], yaw_world[:T]
        ])

        # Track features: [kappa, half_width]
        track_feats = np.column_stack([
            data["kappa"][:T],
            data["half_width"][:T]
        ])

        # Obstacle features (computed on-the-fly from obstacles list)
        obs_feats = self._compute_obstacle_features(
            data["s_m"][:T],
            e[:T],
            ep.get("obstacles_list", [])
        )

        return {
            "states": states.astype(np.float32),
            "track_feats": track_feats.astype(np.float32),
            "obs_feats": obs_feats.astype(np.float32),
            "actions": data["U"][:T].astype(np.float32),
            "rtg": data["rtg"].astype(np.float32),
            "timesteps": np.arange(T, dtype=np.int64),
            "s_abs": data["s_m"][:T].astype(np.float32),
        }

    def _compute_obstacle_features(
        self,
        s_abs: np.ndarray,
        e: np.ndarray,
        obstacles: List[Dict],
    ) -> np.ndarray:
        """
        Compute obstacle features for each timestep.

        Args:
            s_abs: Arc-length positions (T,)
            e: Lateral positions (T,)
            obstacles: List of obstacle dicts with {s_obs, e_obs, r_obs}

        Returns:
            obs_feats: (T, M*3) flattened obstacle features
        """
        T = len(s_abs)
        obs_feats = np.zeros((T, self.obstacle_slots, self.obstacle_feat_dim), dtype=np.float32)
        lap_length = float(s_abs[-1] + (s_abs[1] - s_abs[0])) if T >= 2 else float(s_abs[-1]) if T == 1 else 0.0

        if not obstacles:
            return obs_feats.reshape(T, -1)

        for t in range(T):
            s_t = s_abs[t]
            e_t = e[t]

            # Find obstacles ahead within lookahead window
            ahead_obs = []
            for obs in obstacles:
                s_obs = obs.get("s_obs", obs.get("s", 0))
                e_obs = obs.get("e_obs", obs.get("e", 0))
                r_obs = obs.get("r_obs", obs.get("r", 0))

                ds = s_obs - s_t
                # Handle wrap-around consistently with DT warm-start inference.
                if ds < 0:
                    ds += lap_length
                if 0 < ds <= self.obstacle_lookahead:
                    ahead_obs.append({
                        "ds": ds,
                        "de": e_obs - e_t,
                        "r": r_obs,
                    })

            # Sort by distance and take closest M
            ahead_obs.sort(key=lambda x: x["ds"])
            for i, obs in enumerate(ahead_obs[:self.obstacle_slots]):
                obs_feats[t, i, 0] = obs["ds"]
                obs_feats[t, i, 1] = obs["de"]
                obs_feats[t, i, 2] = obs["r"]

        return obs_feats.reshape(T, -1)

    def _build_augmented_state(self, episode_data: Dict[str, np.ndarray]) -> np.ndarray:
        """Build augmented state representation."""
        return np.concatenate([
            episode_data["states"],
            episode_data["track_feats"],
            episode_data["obs_feats"],
        ], axis=1)

    def _compute_statistics(self) -> DatasetStats:
        """Compute normalization statistics from all episodes."""
        return self.compute_statistics_from_episodes(self.episodes)

    def compute_statistics_from_episodes(self, episodes: List[Dict]) -> DatasetStats:
        """Compute normalization statistics from a provided episode list."""
        print("Computing dataset statistics...")

        all_states = []
        all_actions = []
        all_rtg = []

        # Sample subset for efficiency
        sample_size = min(100, len(episodes))
        sample_indices = np.random.choice(len(episodes), sample_size, replace=False)

        for ep_idx in sample_indices:
            data = self._load_episode_from_record(episodes[ep_idx])
            aug_state = self._build_augmented_state(data)
            all_states.append(aug_state)
            all_actions.append(data["actions"])
            all_rtg.append(data["rtg"])

        all_states = np.concatenate(all_states, axis=0)
        all_actions = np.concatenate(all_actions, axis=0)
        all_rtg = np.concatenate(all_rtg, axis=0)

        stats = DatasetStats(
            state_mean=np.mean(all_states, axis=0),
            state_std=np.std(all_states, axis=0) + 1e-6,
            action_mean=np.mean(all_actions, axis=0),
            action_std=np.std(all_actions, axis=0) + 1e-6,
            rtg_mean=float(np.mean(all_rtg)),
            rtg_std=float(np.std(all_rtg)) + 1e-6,
        )

        print(f"  state_mean shape: {stats.state_mean.shape}")
        print(f"  rtg_mean: {stats.rtg_mean:.4f}, rtg_std: {stats.rtg_std:.4f}")

        return stats

    def __len__(self) -> int:
        return len(self.indices)

    def __getitem__(self, idx: int) -> Dict[str, torch.Tensor]:
        """
        Get a training sample.

        Returns:
            Dict with keys:
                - states: (K, state_aug_dim)
                - actions: (K, act_dim)
                - rtg: (K, 1)
                - timesteps: (K,)
                - attention_mask: (K,)
        """
        ep_idx, start_idx = self.indices[idx]

        # Load episode data
        data = self._load_episode(ep_idx)

        T = len(data["rtg"])
        K = self.context_length

        # Get window indices
        end_idx = min(start_idx + K, T)
        actual_len = end_idx - start_idx

        # Build augmented state
        aug_states = self._build_augmented_state(data)

        # Extract window
        states = aug_states[start_idx:end_idx]
        actions = data["actions"][start_idx:end_idx]
        rtg = data["rtg"][start_idx:end_idx]
        timesteps = data["timesteps"][start_idx:end_idx]

        # Normalize if needed
        if self.normalize and self.stats is not None:
            states = (states - self.stats.state_mean) / self.stats.state_std
            actions = (actions - self.stats.action_mean) / self.stats.action_std
            rtg = (rtg - self.stats.rtg_mean) / self.stats.rtg_std

        # Pad to context length if needed
        if actual_len < K:
            pad_len = K - actual_len
            states = np.concatenate([np.zeros((pad_len, self.state_aug_dim)), states], axis=0)
            actions = np.concatenate([np.zeros((pad_len, self.act_dim)), actions], axis=0)
            rtg = np.concatenate([np.zeros(pad_len), rtg], axis=0)
            timesteps = np.concatenate([np.zeros(pad_len, dtype=np.int64), timesteps], axis=0)
            attention_mask = np.concatenate([np.zeros(pad_len), np.ones(actual_len)], axis=0)
        else:
            attention_mask = np.ones(K)

        # Convert to tensors
        return {
            "states": torch.tensor(states, dtype=torch.float32),
            "actions": torch.tensor(actions, dtype=torch.float32),
            "rtg": torch.tensor(rtg, dtype=torch.float32).unsqueeze(-1),
            "timesteps": torch.tensor(timesteps, dtype=torch.long),
            "attention_mask": torch.tensor(attention_mask, dtype=torch.long),
        }


def create_dataloaders(
    data_dir: Union[str, Path],
    context_length: int = 30,
    batch_size: int = 64,
    num_workers: int = 4,
    normalize: bool = True,
    train_ratio: float = 0.9,
    seed: int = 42,
) -> Tuple[torch.utils.data.DataLoader, torch.utils.data.DataLoader, DatasetStats]:
    """
    Create train and validation dataloaders.

    Args:
        data_dir: Directory containing dataset
        context_length: Context window size K
        batch_size: Batch size
        num_workers: Number of data loading workers
        normalize: Whether to normalize
        train_ratio: Fraction of data for training
        seed: Random seed

    Returns:
        train_loader, val_loader, stats
    """
    # Load full dataset metadata first; split before fitting normalization stats.
    full_dataset = TrajectoryDataset(
        data_dir,
        context_length=context_length,
        normalize=False,
    )

    # Split by base_id to avoid leakage across circular shifts from the same base lap.
    rng = np.random.default_rng(seed)
    episodes_by_base: Dict[str, List[Dict]] = {}
    for ep in full_dataset.episodes:
        base_id = str(ep.get("base_id") or Path(ep["npz_path"]).stem)
        episodes_by_base.setdefault(base_id, []).append(ep)

    base_ids = list(episodes_by_base.keys())
    shuffled_base_ids = [base_ids[i] for i in rng.permutation(len(base_ids))]
    n_train_bases = int(len(shuffled_base_ids) * train_ratio)

    train_base_ids = set(shuffled_base_ids[:n_train_bases])
    val_base_ids = set(shuffled_base_ids[n_train_bases:])

    train_episodes = []
    val_episodes = []
    for base_id, grouped_episodes in episodes_by_base.items():
        if base_id in train_base_ids:
            train_episodes.extend(grouped_episodes)
        elif base_id in val_base_ids:
            val_episodes.extend(grouped_episodes)

    if not train_episodes or not val_episodes:
        raise ValueError(
            "Split by base_id produced an empty train or validation set. "
            "Adjust train_ratio or provide more distinct base laps."
        )

    stats = None
    if normalize:
        stats = full_dataset.compute_statistics_from_episodes(train_episodes)

    # Create separate datasets
    train_dataset = TrajectoryDataset(
        data_dir,
        context_length=context_length,
        normalize=normalize,
        stats=stats,
    )
    train_dataset.episodes = train_episodes
    train_dataset.indices = []
    for ep_idx, ep in enumerate(train_episodes):
        for start in range(ep["length"]):
            train_dataset.indices.append((ep_idx, start))

    val_dataset = TrajectoryDataset(
        data_dir,
        context_length=context_length,
        normalize=normalize,
        stats=stats,
    )
    val_dataset.episodes = val_episodes
    val_dataset.indices = []
    for ep_idx, ep in enumerate(val_episodes):
        for start in range(ep["length"]):
            val_dataset.indices.append((ep_idx, start))

    train_loader = torch.utils.data.DataLoader(
        train_dataset,
        batch_size=batch_size,
        shuffle=True,
        num_workers=num_workers,
        pin_memory=True,
    )

    val_loader = torch.utils.data.DataLoader(
        val_dataset,
        batch_size=batch_size,
        shuffle=False,
        num_workers=num_workers,
        pin_memory=True,
    )

    print(
        f"Train: {len(train_dataset)} samples, {len(train_episodes)} episodes, "
        f"{len(train_base_ids)} base trajectories"
    )
    print(
        f"Val: {len(val_dataset)} samples, {len(val_episodes)} episodes, "
        f"{len(val_base_ids)} base trajectories"
    )

    return train_loader, val_loader, stats


